function getProgressPercent(n) {
    let i = Math.floor((100 * n) / 600);
    if (i >= 100) i = 100;
    return i;
}

function checkInput() {
    const input = document.getElementById('t2').value;
    const target = document.getElementById('t1').value;
    const l1 = target.length, l2 = input.length;

    if (l2 < l1) {
        document.getElementById('t2').value = '';
        document.getElementById('t2').focus();
        return;
    }

    let correct = 0;
    let errorMark = '';
    for (let i = 0; i < l1; i++) {
        if (input[i] === target[i]) {
            correct++;
            errorMark += (target.charCodeAt(i) > 200 ? '.' : '') + '.';
        } else {
            errorMark += (target.charCodeAt(i) > 200 ? '.' : '') + 'x';
        }
    }

    if (correct < l1) {
        document.getElementById('id_err').textContent = errorMark;
    }

    const elapsed = new Date().getTime() - dt;
    const unit = calcTypingUnit(input);
    const speed = Math.floor((unit * 60000) / elapsed);

    console.log("dt : " + dt);
    console.log("elapsed : " + elapsed);
    console.log("unit : " + unit);
    console.log("speed : " + speed);

    if (speed > topt) {
        topt = speed;
        document.getElementById('id_s2').textContent = speed;
        updateProgressBar('id_g2', getProgressPercent(speed));
    }

    document.getElementById('id_s1').textContent = speed;
    updateProgressBar('id_g1', getProgressPercent(speed));
    avt = ((avt * exn) + speed) / (exn + 1);
    document.getElementById('id_s3').textContent = Math.floor(avt);
    updateProgressBar('id_g3', getProgressPercent(avt));

    const acc = correct * 100 / l1;
    document.getElementById('id_s4').textContent = Math.floor(acc);
    updateProgressBar('id_g4', acc);
    avp = (avp * exn + acc) / (exn + 1);
    document.getElementById('id_s5').textContent = Math.floor(avp);
    updateProgressBar('id_g5', avp);

    exn++;
    document.getElementById('t2').value = '';
    nextString();

    start = false;
}

